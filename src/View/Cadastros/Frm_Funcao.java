    public int getCodFuncao() {
        int codfuncao = 0;
        try {
            rs = manager.consulta("SELECT MAX(CODFUNCAO) QTDE FROM FUNCAO");
            while (rs.next()) {
                codfuncao = Integer.parseInt(rs.getString("qtde")) + 1;
            }
        } catch (Exception e) {

        }
        return codfuncao;
    }

    public void salva(String descricao) {
        try {
            if (txt_descricao.getText().replaceAll(" ", "").compareTo("") == 0) {
                JOptionPane.showMessageDialog(null, "Descrição Inválida");
                txt_descricao.setText("");
                txt_descricao.requestFocus();
            } else {
                manager.insere("INSERT INTO FUNCAO (CODFUNCAO,DESCRICAO) VALUES("+getCodFuncao()+",'" + descricao + "')");
                JOptionPane.showMessageDialog(null, "Função Salva com Sucesso!");
                txt_descricao.setText("");
                txt_descricao.requestFocus();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Função Inválida!");
        }
    }

